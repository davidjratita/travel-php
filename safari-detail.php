                                                    <div class="text">
                                                        <p>No other animal reminds us so much of ourselves as the chimpanzee. Besides many common features and mannerisms, chimpanzees also share 98% of our human DNA, making them our closest living relatives. It is not so surprising then that we find the habits and biology of these primates so fascinating to study.</p>
                                                        <p>While Chimpanzees can be found in 21 countries throughout Africa, they are most easily sighted in Uganda due to the high concentration of the species here. On a Chimpanzee Tracking experience in Kibale Forest you will have the opportunity to follow an experienced guide into the forest to observe these fascinating creatures at a close range. Watching them interact with each other and go about their daily routine of grooming, playing and foraging is an incredible experience.</p>
                                                        <p>While your chances of encountering chimpanzees are high during your tracking excursion, it is never 100% guaranteed. These are, after all, wild animals with wills and minds of their own!
                                                        In the afternoon you will have time to enjoy the lodge which boasts a pool with beautiful views of the surrounding landscapes.</p>
