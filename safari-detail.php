                                                    <div class="text">
                                                        <p>In the morning we offer you an opportunity to contribute to an important conservation program in Queen Elizabeth NP. On this unique Lion Tracking research trip you will follow a research team, off the safari jeep tracks and into the bush, in search of one of the lion prides in the park. A tracking device in the vehicle will leady you directly to the lions where you will spend some time observing these charismatic cats. The data collected by the researchers will further the conservation program in the park, while you get a unique and intimate insight into the lives and habits of these fascinating predators.</p>
                                                        <p>Later in the day when the sun is high in the sky and the animals make their way to the river for a drink, we will head to Kazinga Channel for a private boat safari. You will spot more hippos today than you can possibly imagine. Herds of elephants and buffalo will also wander down to the shoreline to bathe in the cooling waters. A boat safari is a fantastic way to get close to wildlife and for many visitors the boat safari is the highlight of their visit to Queen Elizabeth National Park.</p>
                                                        
